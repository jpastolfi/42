#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
// 23. Execute the high order function to current node content.
// 24. Now the lst node should be the next until it gets to NULL.
