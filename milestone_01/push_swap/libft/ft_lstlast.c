#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
// 17. Parameter function is one node in any place of a list
// 21. When lst->next is NULL, return current position (last node) of the lst
